f5 = '{}乘舟将欲行，忽闻岸上踏歌声'
a = '李白'

#1、普通方式
print('{}乘舟将欲行，忽闻岸上踏歌声'.format(a)) #字符串a将{}内的内容替换掉

#2、通过索引传参
print('{2}乘舟将欲行，忽闻岸上踏歌声{1}'.format('a','b','c'))

#3、关键字传参
print('{a}乘舟将欲行，忽闻岸上踏歌声'.format(a='小四'))

#4、容器类型数据传参
print('豪放派:{},婉约派:{},蛋黄派:{}'.format(['李白'],['辛弃疾'],['达利园']))

print(f5.find('忽'))
print(f5.index('忽'))